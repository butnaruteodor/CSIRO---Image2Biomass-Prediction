"""
plot_convergence.py — Mean OOF Weighted R² vs. Epoch for each CV protocol.

Loads results/experiment_2/per_epoch_metrics.pt (generated by experiment_2.py)
and plots the mean weighted R² over folds and seeds as a function of training epoch
for each validation protocol.

Usage:
    python figures/plot_convergence.py
    
Output:
    figures/convergence_curves.pdf  (publication-quality vector figure)
    figures/convergence_curves.png  (raster preview)
"""

import os, sys
import numpy as np
import pandas as pd
import torch
import matplotlib
matplotlib.use('Agg')           # non-interactive backend for headless use
import matplotlib.pyplot as plt
import matplotlib.ticker as mticker
from pathlib import Path

# ── Paths ────────────────────────────────────────────────────────────────────
RESULTS_DIR = Path('results/experiment_2')
PER_EPOCH_FILE = RESULTS_DIR / 'per_epoch_metrics.pt'
FULL_RESULTS_FILE = RESULTS_DIR / 'full_results.pt'
OUTPUT_DIR = Path('figures')
OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

PUBLICATION_NAMES = {
    'random_stratified':       'Random CV',
    'date_grouped':            'Date CV',
    'date_location_grouped':   'Date-State CV',
    'leave_one_period_out':    'LOPO CV',
}

# Colors (colorblind-friendly, consistent with paper style)
PROTOCOL_COLORS = {
    'random_stratified':       '#0072B2',   # blue
    'date_grouped':            '#009E73',   # green
    'date_location_grouped':   '#D55E00',   # orange / vermillion
    'leave_one_period_out':    '#CC79A7',   # reddish pink
}

# ── Load data ────────────────────────────────────────────────────────────────

def load_per_epoch_data():
    """
    Load per-epoch metrics from the dedicated file.
    Falls back to reconstructing from full_results.pt if the per-epoch file
    does not exist yet (e.g. experiment still running).
    """
    if PER_EPOCH_FILE.exists():
        print(f"Loading per-epoch metrics from {PER_EPOCH_FILE}")
        return torch.load(PER_EPOCH_FILE, map_location='cpu', weights_only=False)
    
    # ── Fallback: reconstruct from fold_results in full_results.pt ──────────
    if FULL_RESULTS_FILE.exists():
        print(f"Per-epoch file not found. Reconstructing from {FULL_RESULTS_FILE} ...")
        print("NOTE: This will only provide the BEST epoch, not the full curve.")
        print("Run experiment_2.py to completion to get full per-epoch data.")
        all_results = torch.load(FULL_RESULTS_FILE, map_location='cpu', weights_only=False)
        
        # Build minimal per-epoch dict with just the best epoch
        all_per_epoch = {}
        for protocol_name, protocol_data in all_results.items():
            protocol_epochs = []
            for fr in protocol_data.get('fold_results', []):
                protocol_epochs.append({
                    'protocol': protocol_name,
                    'seed': fr['seed'],
                    'fold': fr['fold'],
                    'epoch': fr['best_epoch'],
                    'weighted_r2': fr['weighted_r2'],
                })
            all_per_epoch[protocol_name] = protocol_epochs
        return all_per_epoch
    
    raise FileNotFoundError(
        "Neither per_epoch_metrics.pt nor full_results.pt found "
        f"in {RESULTS_DIR}. Run experiment_2.py first."
    )


# ── Plotting ─────────────────────────────────────────────────────────────────

def plot_convergence(all_per_epoch):
    """Plot mean weighted R² vs. epoch for each protocol with ±1 std shading."""
    
    # Convert to a flat DataFrame
    rows = []
    for protocol_name, epoch_list in all_per_epoch.items():
        if protocol_name not in PUBLICATION_NAMES:
            continue  # skip any protocols not in our display map
        for entry in epoch_list:
            rows.append({
                'protocol': protocol_name,
                'epoch': entry['epoch'],
                'weighted_r2': entry['weighted_r2'],
            })
    
    df = pd.DataFrame(rows)
    print(f"Loaded {len(df)} per-epoch measurements across {df['protocol'].nunique()} protocols.")
    
    # Group by protocol + epoch, compute mean and std across folds × seeds
    grouped = (
        df.groupby(['protocol', 'epoch'])['weighted_r2']
        .agg(['mean', 'std', 'count'])
        .reset_index()
    )
    grouped.rename(columns={'mean': 'avg_r2', 'std': 'std_r2', 'count': 'n'}, inplace=True)
    
    # Calculate standard error (over folds × seeds)
    grouped['se_r2'] = grouped['std_r2'] / np.sqrt(grouped['n'])
    
    # ── Create the plot ──────────────────────────────────────────────────────
    fig, ax = plt.subplots(1, 1, figsize=(7.5, 5.5))
    
    protocol_order = ['date_location_grouped', 'date_grouped', 'random_stratified', 'leave_one_period_out']
    
    for protocol_name in protocol_order:
        if protocol_name not in grouped['protocol'].unique():
            print(f"  WARNING: {protocol_name} not found in data, skipping.")
            continue
        
        sub = grouped[grouped['protocol'] == protocol_name].sort_values('epoch')
        epochs = sub['epoch'].values
        mean_r2 = sub['avg_r2'].values
        # Use ±1 std (not SE) to show variability across folds/seeds
        std_r2 = sub['std_r2'].values
        
        label = PUBLICATION_NAMES[protocol_name]
        color = PROTOCOL_COLORS[protocol_name]
        
        # Main line
        ax.plot(epochs, mean_r2, color=color, linewidth=2.0, label=label, zorder=3)
        # Shaded region: ±1 std
        ax.fill_between(epochs, mean_r2 - std_r2, mean_r2 + std_r2,
                        color=color, alpha=0.15, linewidth=0)
    
    # ── Annotations ──────────────────────────────────────────────────────────
    # Vertical dashed lines at epoch 20 and 35
    ax.axvline(x=20, color='gray', linestyle='--', linewidth=0.8, alpha=0.5)
    ax.axvline(x=35, color='gray', linestyle='--', linewidth=0.8, alpha=0.5)
    
    # Text labels for the vertical lines
    ax.text(20, ax.get_ylim()[0] + 0.02, 'epoch 20', ha='center', va='bottom',
            fontsize=8, color='gray', alpha=0.7, style='italic')
    ax.text(35, ax.get_ylim()[0] + 0.02, 'epoch 35', ha='center', va='bottom',
            fontsize=8, color='gray', alpha=0.7, style='italic')
    
    # ── Labels and styling ──────────────────────────────────────────────────
    ax.set_xlabel('Training epoch', fontsize=12)
    ax.set_ylabel('Mean OOF weighted R²', fontsize=12)
    ax.set_title('Convergence of Validation Protocols', fontsize=13, fontweight='bold')
    
    # Legend
    ax.legend(fontsize=10, loc='lower right', frameon=True, fancybox=True,
              framealpha=0.9, edgecolor='#cccccc')
    
    # Grid
    ax.grid(True, alpha=0.3, linestyle=':', linewidth=0.5)
    ax.set_axisbelow(True)
    
    # Axis limits (tight but with some padding)
    ax.set_xlim(0, None)
    ax.set_ylim(0.0, 1.0)  # R² is bounded by [0, 1] for reasonable models
    
    # Ticks
    ax.xaxis.set_major_locator(mticker.MultipleLocator(10))
    ax.xaxis.set_minor_locator(mticker.MultipleLocator(5))
    ax.yaxis.set_major_locator(mticker.MultipleLocator(0.2))
    ax.yaxis.set_minor_locator(mticker.MultipleLocator(0.1))
    
    # Make it publication-quality
    plt.tight_layout(pad=1.0)
    
    # Save
    pdf_path = OUTPUT_DIR / 'convergence_curves.pdf'
    png_path = OUTPUT_DIR / 'convergence_curves.png'
    
    fig.savefig(pdf_path, dpi=300, bbox_inches='tight', pad_inches=0.1)
    fig.savefig(png_path, dpi=200, bbox_inches='tight', pad_inches=0.1)
    
    plt.close(fig)
    
    print(f"\nPlot saved to:")
    print(f"  {pdf_path}")
    print(f"  {png_path}")
    
    # ── Print summary statistics ─────────────────────────────────────────────
    print("\n" + "=" * 70)
    print("Summary: Mean R² at key epochs (± std)")
    print("=" * 70)
    
    for protocol_name in protocol_order:
        if protocol_name not in grouped['protocol'].unique():
            continue
        sub = grouped[grouped['protocol'] == protocol_name]
        display = PUBLICATION_NAMES[protocol_name]
        
        # Find values at epoch 20, 35, and max
        epoch_20 = sub[sub['epoch'] == 20]
        epoch_35 = sub[sub['epoch'] == 35]
        max_row = sub.loc[sub['avg_r2'].idxmax()]
        
        r2_20 = epoch_20['avg_r2'].values[0] if len(epoch_20) > 0 else np.nan
        r2_35 = epoch_35['avg_r2'].values[0] if len(epoch_35) > 0 else np.nan
        r2_max = max_row['avg_r2']
        ep_max = max_row['epoch']
        
        print(f"  {display:<20}  @20: {r2_20:.4f}  @35: {r2_35:.4f}  "
              f"max: {r2_max:.4f} @ ep {ep_max:.0f}")


# ── Main ─────────────────────────────────────────────────────────────────────

if __name__ == '__main__':
    all_per_epoch = load_per_epoch_data()
    plot_convergence(all_per_epoch)
    print("\nDone.")